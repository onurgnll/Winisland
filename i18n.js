function normalizeLocale(locale) {
  const base = String(locale || 'en').toLowerCase().split(/[-_]/)[0];
  return SUPPORTED_LOCALES.includes(base) ? base : 'en';
}

function t(key, locale, params) {
  const loc = normalizeLocale(locale);
  let str = MESSAGES[loc]?.[key] ?? MESSAGES.en[key] ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    });
  }
  return str;
}

function getSystemLocale(appLocale) {
  return normalizeLocale(appLocale);
}

function intlLocale(locale) {
  const loc = normalizeLocale(locale);
  return loc === 'tr' ? 'tr-TR' : 'en-US';
}

